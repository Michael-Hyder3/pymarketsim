    def _run_diagnostic(
        self,
        candidates: List[str],
        episode_number: int,
        config: MarketConfig,
    ) -> Tuple[str, Dict[str, Any]]:
        """
        Diagnostic selection: run ONE sim per candidate at the config's fixed seed.
        Winner = highest surplus.  Returns (winning_code, diagnostic_summary).

        The fixed seed guarantees all candidates face the same market draw, making
        the surplus comparison fair and reproducible.  The full diagnostic_json
        (capture_detail=True) is collected for the winning candidate and stored as
        the context for the next episode's prompt.
        """
        import numpy as _np
        import math as _math

        # Generate the fixed seed list used for ALL candidates — same 50 seeds every time
        base_seed = config.diagnostic_seed(episode_number)
        seeds = [base_seed + i for i in range(self.num_eval_seeds)]

        print(f"\n[Diagnostic] Evaluating {len(candidates)} candidates over "
              f"{self.num_eval_seeds} seeds (base={base_seed}, config={config}) ...")

        best_code = candidates[0]
        best_avg_surplus = -float('inf')
        best_ci = 0
        best_batch: Optional[Dict] = None
        results = []
        # Consumer surplus depends only on private values drawn from each seed,
        # not on the strategy — so it is identical across all candidates.
        # We collect it once from candidate 0 and reuse for the summary.
        shared_consumer_surpluses: List[float] = []
        shared_consumer_avg: Optional[float] = None

        for ci, code in enumerate(candidates):
            namespace = {"np": _np, "numpy": _np, "math": _math}
            exec(code, namespace)
            strategy_func = namespace['agent_strategy']
            reasoning = self._extract_reasoning(code) or "(no docstring)"

            surpluses: List[float] = []
            zi_surpluses: List[float] = []
            per_units: List[float] = []
            shades: List[float] = []
            units_exec_list: List[int] = []
            positive_pv_counts: List[int] = []  # number of units with pv > 0 per seed
            last_result: Optional[Dict] = None

            for seed in seeds:
                try:
                    r = run_episode(
                        llm_strategy_func=strategy_func,
                        llm_order_type=self.llm_order_type,
                        llm_private_values=config.fixed_private_values_relative,
                        num_zi_buy=config.num_zi_buy,
                        num_zi_sell=config.num_zi_sell,
                        timesteps=config.timesteps,
                        q_max=config.q_max,
                        pv_var=config.pv_var,
                        shade_range=config.shade_range,
                        random_seed=seed,
                        capture_detail=(seed == base_seed),  # capture detail only for first seed
                    )
                except Exception as e:
                    print(f"  Candidate {ci+1} seed {seed}: crashed ({type(e).__name__}: {e}) — skipping")
                    continue
                surpluses.append(r["llm_surplus"])
                if ci == 0:
                    shared_consumer_surpluses.append(r.get("consumer_surplus", 0.0))
                zi_surpluses.append(r["avg_zi_same_surplus"])
                per_units.append(r["per_unit_surplus"])
                if r["avg_shade"] is not None:
                    shades.append(r["avg_shade"])
                units_exec_list.append(r["llm_units_executed"])
                # Count units with positive private value — this is the natural exec target
                pvs = r.get("llm_private_values_relative", [])
                positive_pv_counts.append(sum(1 for pv in pvs if pv > 0))
                if seed == base_seed:
                    last_result = r

            if not surpluses:
                print(f"  Candidate {ci+1}: all seeds crashed — disqualified")
                results.append({"candidate": ci+1, "avg_surplus": None, "reasoning": reasoning})
                continue

            avg_surplus = sum(surpluses) / len(surpluses)
            std_surplus = (sum((s - avg_surplus)**2 for s in surpluses) / len(surpluses))**0.5
            if ci == 0 and shared_consumer_surpluses:
                shared_consumer_avg = sum(shared_consumer_surpluses) / len(shared_consumer_surpluses)
            avg_zi = sum(zi_surpluses) / len(zi_surpluses)
            avg_per_unit = sum(per_units) / len(per_units)
            avg_shade_val = sum(shades) / len(shades) if shades else None
            avg_units = sum(units_exec_list) / len(units_exec_list)
            avg_exec_ratio = avg_units / config.q_max
            # Neutral exec ratio: avg fraction of units with pv > 0 — no bonus/penalty here
            avg_positive_pvs = sum(positive_pv_counts) / len(positive_pv_counts) if positive_pv_counts else config.q_max
            neutral_exec_ratio = avg_positive_pvs / config.q_max

            # Phase-1 simple objective: raw average surplus is the selection score.
            shaped_score = avg_surplus
            cs_pct = f"{avg_surplus / shared_consumer_avg * 100:.0f}%" if shared_consumer_avg else "N/A"

            batch_summary = {
                "avg_surplus": round(avg_surplus, 4),
                "selection_score": round(shaped_score, 4),
                "std_surplus": round(std_surplus, 4),
                "avg_zi_same_surplus": round(avg_zi, 4),
                "avg_per_unit_surplus": round(avg_per_unit, 4),
                "avg_shade": round(avg_shade_val, 4) if avg_shade_val is not None else None,
                "avg_units_executed": round(avg_units, 2),
                "avg_exec_ratio": round(avg_exec_ratio, 4),
                "num_seeds": len(surpluses),
                "last_result": last_result,  # detail run from base_seed
            }

            marker = ""
            if shaped_score > best_avg_surplus:
                best_avg_surplus = shaped_score
                best_code = code
                best_ci = ci
                best_batch = batch_summary
                marker = " ◀ leading"

            beat = "▲" if avg_surplus > avg_zi else "▼"
            print(f"  Candidate {ci+1}: avg=${avg_surplus:.2f} ±{std_surplus:.0f}  "
                  f"cs={cs_pct}  zi=${avg_zi:.0f} {beat}  "
                  f"exec={avg_exec_ratio*100:.0f}%  {reasoning[:50]}{marker}")
            results.append({"candidate": ci+1, "reasoning": reasoning, **batch_summary})

            best_raw_surplus = (best_batch or {}).get("avg_surplus", best_avg_surplus)
            best_selection = (best_batch or {}).get("selection_score", best_avg_surplus)
        cs_ratio = f"  ({best_raw_surplus/shared_consumer_avg*100:.0f}% of CS)" if shared_consumer_avg else ""
        print(f"  → Winner: Candidate {best_ci + 1}  raw=${best_raw_surplus:.2f}  "
              f"score={best_selection:.1f}{cs_ratio}")

        if shared_consumer_avg is not None:
            print(f"  (Consumer surplus: ${shared_consumer_avg:.2f} — same for all candidates)")

        return best_code, {
            "diagnostic_results": results,
            "winner_avg_surplus": best_raw_surplus,   # raw surplus for value table / buffer
            "winner_selection_score": best_selection,
            "winner_batch": best_batch,
            "shared_consumer_surplus": round(shared_consumer_avg, 4) if shared_consumer_avg is not None else None,
            "base_seed": base_seed,
            "num_eval_seeds": self.num_eval_seeds,
        }

    def _run_batches(
        self,
        strategy_func: Callable,
        episode_number: int,
        config: Optional[MarketConfig] = None,
    ) -> Dict[str, Any]:
        """
        Retained for backwards-compatibility but no longer called in the main loop.
        Can be used externally for extended evaluation of a winner.
        """
        if config is None:
            config = self.market_configs[0]
        num_batches = 10  # small default for manual use
        surpluses: List[float] = []
        per_units: List[float] = []
        exec_ratios: List[float] = []
        shades: List[float] = []
        zi_surpluses: List[float] = []
        total_units_exec = 0

        for b in range(num_batches):
            seed = episode_number * 1000 + b
            try:
                result = run_episode(
                    llm_strategy_func=strategy_func,
                    llm_order_type=self.llm_order_type,
                    llm_private_values=config.fixed_private_values_relative,
                    num_zi_buy=config.num_zi_buy,
                    num_zi_sell=config.num_zi_sell,
                    timesteps=config.timesteps,
                    q_max=config.q_max,
                    pv_var=config.pv_var,
                    shade_range=config.shade_range,
                    random_seed=seed,
                )
            except (RecursionError, Exception) as e:
                print(f"  [Batch {b}] crashed: {type(e).__name__}: {e} — skipping")
                continue
            surpluses.append(result["llm_surplus"])
            per_units.append(result["per_unit_surplus"])
            exec_ratios.append(result["episode_stats"]["execution_ratio"])
            if result["avg_shade"] is not None:
                shades.append(result["avg_shade"])
            total_units_exec += result["llm_units_executed"]
            zi_surpluses.append(result["avg_zi_same_surplus"])

        if not surpluses:
            return {"avg_surplus": 0.0, "std_surplus": 0.0, "avg_per_unit": 0.0,
                    "avg_exec_ratio": 0.0, "avg_shade": None, "total_units_exec": 0,
                    "num_batches": 0, "avg_zi_same_surplus": 0.0}

        avg_surplus = sum(surpluses) / len(surpluses)
        std_surplus = (sum((s - avg_surplus) ** 2 for s in surpluses) / len(surpluses)) ** 0.5
        avg_zi = sum(zi_surpluses) / len(zi_surpluses)

        return {
            "avg_surplus": avg_surplus,
            "std_surplus": std_surplus,
            "avg_per_unit": sum(per_units) / len(per_units),
            "avg_exec_ratio": sum(exec_ratios) / len(exec_ratios),
            "avg_shade": sum(shades) / len(shades) if shades else None,
            "total_units_exec": total_units_exec,
            "num_batches": num_batches,
            "avg_zi_same_surplus": avg_zi,
        }

