"""
Feedback Loop Pipeline: Multi-state RL architecture for LLM-driven market agent optimization.

RL framing:
  State  s  = MarketConfig (market parameters: timesteps, q_max, shade_range, …)
  Action a  = strategy code (Python function generated by the LLM)
  Reward R  = surplus earned by strategy a in the diagnostic run for state s
  Policy π  = LLM: given (s, diagnostic_json, value_table) → propose new strategy
  Value  V  = ValueTable: V(s) ≈ max_{a seen} R(s, a)

Outer Loop: Episodes (k = 1, 2, …, K)
  - Pick next state s_k from the config rotation
  - Generate N candidate strategies (policy inference)
  - Run one diagnostic sim per candidate at the fixed seed for s_k
  - Select winner = highest surplus
  - Update V(s_k) with winner surplus
  - Store diagnostic JSON in buffer for next episode's context

Inner Loop: Code refinement (per candidate, up to M attempts)
  - Verify strategy passes unit tests
  - On failure, show error and retry
"""

