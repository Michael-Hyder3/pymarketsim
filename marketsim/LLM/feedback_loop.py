    def _resolve_winner_result(
        self,
        best_code: str,
        episode_number: int,
        config: MarketConfig,
        diagnostic_summary: Optional[Dict[str, Any]],
    ) -> Tuple[Optional[Dict[str, Any]], Dict[str, Any]]:
        """Return (winner_batch, winner_result), running fallback diagnostic when needed."""
        winner_batch = (diagnostic_summary or {}).get("winner_batch")
        winner_result = (winner_batch or {}).get("last_result") if winner_batch else None

        if winner_result is not None:
            return winner_batch, winner_result

        import numpy as _np
        import math as _math
        ns = {"np": _np, "numpy": _np, "math": _math}
        exec(best_code, ns)
        try:
            winner_result = run_episode(
                llm_strategy_func=ns['agent_strategy'],
                llm_order_type=self.llm_order_type,
                llm_private_values=config.fixed_private_values_relative,
                num_zi_buy=config.num_zi_buy,
                num_zi_sell=config.num_zi_sell,
                timesteps=config.timesteps,
                q_max=config.q_max,
                pv_var=config.pv_var,
                shade_range=config.shade_range,
                random_seed=config.diagnostic_seed(episode_number),
                capture_detail=True,
            )
        except Exception as e:
            print(f"[Episode] Warning: winner diagnostic failed: {e}")
            winner_result = {}
        return None, winner_result

    @staticmethod
    def _winner_family_from_summary(
        diagnostic_summary: Optional[Dict[str, Any]],
        utility: float,
        candidate_family_tags: List[str],
        fallback_family: Optional[str],
    ) -> str:
        """Infer winner family tag from diagnostic summary, with safe fallback."""
        diag_results = (diagnostic_summary or {}).get("diagnostic_results", [])
        winner_ci = next(
            (
                r["candidate"] - 1
                for r in diag_results
                if r.get("avg_surplus") is not None and abs(r["avg_surplus"] - utility) < 0.01
            ),
            0,
        )
        if candidate_family_tags and winner_ci < len(candidate_family_tags):
            return candidate_family_tags[winner_ci]
        return fallback_family or "unknown"

    def run_episode(self, episode_number: int) -> Dict[str, Any]:
        """
        Run a single episode.

        RL step:
          1. Pick state s_k = market_configs[k % len(configs)]
          2. Generate N candidate strategies (policy π inference)
          3. Run ONE diagnostic sim per candidate at fixed seed (same for all) → rewards
          4. Select winner = highest surplus
          5. Update value table V(s_k)
          6. Store diagnostic JSON in buffer for next episode's context
        """
        # Pick state for this episode (round-robin)
        config = self.market_configs[self.episode_count % len(self.market_configs)]

        print(f"\n{'='*60}")
        print(f"EPISODE {episode_number}  |  State: {config}")
        print(f"{'='*60}")

        best_code = None
        diagnostic_summary = None

        candidate_family_tags: List[str] = []
        if self.num_candidates > 1:
            print(f"\n[Policy] Requesting {self.num_candidates} candidate strategies...")
            verified_candidates, candidate_family_tags = self.generate_candidate_strategies(self.num_candidates, config=config)

            if len(verified_candidates) >= 2:
                best_code, diagnostic_summary = self._run_diagnostic(
                    verified_candidates, episode_number, config
                )
            elif len(verified_candidates) == 1:
                print("[Policy] Only 1 candidate passed verification.")
                best_code = verified_candidates[0]
                # Still run diagnostic on the single candidate to get diagnostic_json
                _, diagnostic_summary = self._run_diagnostic([best_code], episode_number, config)
            else:
                print("[Policy] No candidates passed, falling back to single-strategy loop.")

        if best_code is None:
            error_feedback = None
            previous_attempt = None
            for inner_iter in range(self.max_inner_iterations):
                print(f"\n[Inner Loop {inner_iter}] Generating strategy...")
                candidate_code = self.generate_strategy_code(
                    iteration=inner_iter,
                    error_feedback=error_feedback,
                    previous_attempt=previous_attempt,
                    config=config,
                )
                passes, error_trace = self.verify_strategy(candidate_code, iteration=inner_iter)
                if passes:
                    print(f"[Inner Loop {inner_iter}] ✓ Strategy verified!")
                    best_code = candidate_code
                    break
                else:
                    print(f"[Inner Loop {inner_iter}] ✗ Verification failed: {error_trace}")
                    error_feedback = error_trace
                    previous_attempt = candidate_code

        if best_code is None:
            print(f"Failed to generate any verified strategy for episode {episode_number}")
            return {"episode_number": episode_number, "success": False,
                    "error": "Could not generate verified code"}

        # ── Get final diagnostic result for the winner ──────────────────
        winner_batch, winner_result = self._resolve_winner_result(
            best_code=best_code,
            episode_number=episode_number,
            config=config,
            diagnostic_summary=diagnostic_summary,
        )

        metrics = self._summarize_winner_metrics(
            winner_batch=winner_batch,
            diagnostic_summary=diagnostic_summary,
            winner_result=winner_result,
            q_max=config.q_max,
            num_eval_seeds=self.num_eval_seeds,
        )
        utility = metrics["utility"]
        avg_zi = metrics["avg_zi"]
        per_unit_surplus = metrics["per_unit_surplus"]
        avg_shade = metrics["avg_shade"]
        units_executed = metrics["units_executed"]
        execution_ratio = metrics["execution_ratio"]

        self._print_winner_metrics(metrics)

        # ── Update value table V(s) ─────────────────────────────────────
        is_new_best = self.value_table.update(config, episode_number, utility, best_code)
        if is_new_best:
            print(f"  ★ New best for state '{config.label or config.config_hash()}' (avg=${utility:.2f})!")

        # ── Update scored population (FunSearch island) ─────────────────
        winner_family = self._winner_family_from_summary(
            diagnostic_summary=diagnostic_summary,
            utility=utility,
            candidate_family_tags=candidate_family_tags,
            fallback_family=self.last_winner_family,
        )
        self._get_population(config).add(best_code, utility, winner_family, episode_number)
        self.last_winner_family = winner_family

        # ── Update UCB bandit with winner surplus ────────────────────────
        self._get_bandit(config).update(winner_family, utility)
        bandit_summary = self._get_bandit(config).summary()
        top_family = max(bandit_summary, key=lambda f: bandit_summary[f]["avg_surplus"])
        print(f"  [Bandit] winner_family='{winner_family}'  top_family='{top_family}'  "
              f"pop_size={len(self._get_population(config))}")

        # ── Update OPRO surplus history ──────────────────────────────────
        self._surplus_history.append((episode_number, utility))

        # ── Store in buffer for next episode's context ──────────────────
        self.buffer.add_episode(
            episode_number=episode_number,
            strategy_code=best_code,
            utility=utility,
            units_executed=units_executed,
            execution_ratio=execution_ratio,
            metadata={
                "config_hash": config.config_hash(),
                "config_label": config.label,
                "zi_opponents": config.num_zi_buy,
                "timesteps": config.timesteps,
                "per_unit_surplus": per_unit_surplus,
                "avg_shade": avg_shade,
            }
        )

        # ── Save diagnostic JSON for next episode's context ─────────────
        self.last_diagnostic_json = winner_result
        self.last_winner_reasoning = self._extract_reasoning(best_code)
        self.last_winner_code = best_code
        # last_winner_family already set above

        self.episode_count += 1
        result = {
            "episode_number": episode_number,
            "success": True,
            "utility": utility,
            "units_executed": units_executed,
            "execution_ratio": execution_ratio,
            "per_unit_surplus": per_unit_surplus,
            "avg_shade": avg_shade,
            "avg_zi_same_surplus": avg_zi,
            "strategy_code": best_code,
            "strategy_reasoning": self.last_winner_reasoning,
            "config": config.to_dict(),
            "diagnostic_summary": diagnostic_summary,
        }
        self.episode_results.append(result)
        return result

    def run_feedback_loop(self, num_episodes: int = 5) -> Dict[str, Any]:
        """
        Run the complete multi-state RL feedback loop for num_episodes episodes.

        Each episode:
          - Picks the next MarketConfig (state) round-robin
          - Generates N candidate strategies (policy inference)
          - Runs ONE diagnostic sim per candidate (same seed for all candidates)
          - Selects winner by surplus (reward), updates value table V(s)
          - Stores result in rolling buffer for next episode's context
        """
        print(f"\n{'='*70}")
        print(f"MULTI-STATE RL FEEDBACK LOOP PIPELINE")
        print(f"{'='*70}")
        print(f"Configuration:")
        print(f"  Episodes:          {num_episodes}")
        print(f"  Agent Type:        {'Buyer' if self.llm_order_type == BUY else 'Seller'}")
        print(f"  q_max:             {self.q_max}")
        print(f"  Candidates / ep:   {self.num_candidates}")
        print(f"  Eval seeds / cand: {self.num_eval_seeds}")
        print(f"  Max retries / cand:{self.max_inner_iterations}")
        print(f"  Buffer Size:       {self.buffer.buffer_size}")
        print(f"  State Space ({len(self.market_configs)} configs):")
        for cfg in self.market_configs:
            print(f"    {cfg}")

        for k in range(1, num_episodes + 1):
            result = self.run_episode(k)

            if result["success"]:
                episodes, utilities = self.buffer.get_utility_trend()
                best_util = self.buffer.get_best_utility()
                utils_fmt = [f"${u:.0f}" for u in utilities]
                print(f"\n[Buffer] Episodes: {episodes}  surpluses: {utils_fmt}  best: ${best_util:.2f}")
                print(f"[Value Table]\n{self.value_table.summary_table()}")

        # Final summary
        print(f"\n{'='*70}")
        print(f"FEEDBACK LOOP COMPLETE")
        print(f"{'='*70}")
        episodes, utilities = self.buffer.get_utility_trend()
        print(f"  Episodes Completed: {self.episode_count}")
        print(f"  Buffer: {len(self.buffer)} / {self.buffer.buffer_size} entries")
        print(f"  Surplus Trend: {[f'${u:.0f}' for u in utilities]}")
        print(f"  Best Surplus: ${self.buffer.get_best_utility():.2f}")
        print(f"\n[Final Value Table V(s)]")
        print(self.value_table.summary_table())

        return {
            "num_episodes": num_episodes,
            "episode_count": self.episode_count,
            "buffer": {
                "size": len(self.buffer),
                "max_size": self.buffer.buffer_size,
                "utilities": utilities,
                "episodes": episodes,
                "average_utility": self.buffer.get_average_utility(),
                "best_utility": self.buffer.get_best_utility(),
            },
            "value_table": json.loads(self.value_table.to_json()),
            "episode_results": self.episode_results,
        }
